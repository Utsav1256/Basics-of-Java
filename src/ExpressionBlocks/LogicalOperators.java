package ExpressionBlocks;

public class LogicalOperators {

	public static void main(String[] args) {

//		Logical Operators are used to check whether an expression is true / false.
//		They are used in Descision making.
		
//		1. && (logical AND) expression1 && expression2
//		true only if both exp1 and exp2 are true
		System.out.println((5>3) && (6<7));
		System.out.println((5<3) && (6<7));
		
	}

}










